<h1>Irrigantion</h1>
<img src="images/irrigation.webp" width="320" height="300" align="left" style="margin:10px; border:#666 solid 1px;" />
Irrigation is the practice of applying controlled amounts of water to land to help grow crops, landscape plants, and lawns. Irrigation has been a key 
aspect of agriculture for over 5,000 years and has been developed by many cultures around the world. Irrigation helps to grow crops, maintain landscapes, 
and revegetate disturbed soils in dry areas and during times of below-average rainfall. In addition to these uses, irrigation is also employed to protect 
crops from frost, suppress weed growth in grain fields, and prevent soil consolidation. It is also used to cool livestock, reduce dust, dispose of sewage, 
and support mining operations. Drainage, which involves the removal of surface and sub-surface water from a given location, is often studied in conjunction 
with irrigation.

There are several methods of irrigation that differ in how water is supplied to plants. Surface irrigation, also known as gravity irrigation, is the oldest 
form of irrigation and has been in use for thousands of years. In sprinkler irrigation, water is piped to one or more central locations within the field and
 distributed by overhead high-pressure water devices. Micro-irrigation is a system that distributes water under low pressure through a piped network and 
applies it as a small discharge to each plant. Micro-irrigation uses less pressure and water flow than sprinkler irrigation. Drip irrigation delivers water 
directly to the root zone of plants.


