<h1>Seeding</h1>
<img src="images/plants.jpg" width="320" height="300" align="right" style="margin-left:10px; border:#666 solid 1px;" />
The fundamental factor in the process of seeding is dependent on the properties of both seed and the soil it is being planted in. The prior step associated
 with seeding is crop selection, which mainly consists of two techniques: sexual and asexual. Asexual technique includes all forms of the vegetative process
 such as budding, grafting and layering. Sexual technique involves growing of the plant from a seed. Grafting is referred to as the artificial method of 
propagation in which parts of plants are joined together in order to make them bind together and continue growing as one plant. Grafting is mainly applied 
to two parts of the plant: the dicot and the gymnosperms due to the presence of vascular cambium between the plant tissues: xylem and phloem. A grafted 
plant consists of two parts: first rootstock, which is the lower part of the plant that comprises roots and the lowest part of the shoot; second, the 
branches and primary stem, which consists of the upper and main part of the shoot which gradually develops into a fully nourished plant. Budding is 
another form of asexual reproduction in which the new plant develops from a productive objective source of the parent plant. It is a method in which a bud
 from the plant is joined onto the stem of another plant The plant in which the bud is implanted in eventually develops into a replica of the parent 
plant. The new plant can either divert its ways into forming an independent plant; however, in numerous cases it may remain attached and form various 
accumulations.



