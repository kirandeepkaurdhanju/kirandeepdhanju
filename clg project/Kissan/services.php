<h1>Seedling</h1>
<img src="images/seedling.webp" width="320" height="300" align="left" style="margin-right:10px; border:#666 solid 1px;"/>

Germination is a process by which the seed develops into a seedling. The vital conditions necessary for this process are water, air, temperature, 
energy, viability and enzymes. If any of these conditions are absent, the process cannot undergo successfully. Germination is also known as sprouting; <br />
it is also considered the first sign of life shown by a seed.A seedling is a young sporophyte developing out of a plant embryo from a seed. Seedling development starts with germination of the seed. A typical young seedling consists of three main parts: the radicle (embryonic root), the hypocotyl (embryonic shoot), and the cotyledons (seed leaves). The two classes of flowering plants (angiosperms) are distinguished by their numbers of seed leaves: monocotyledons (monocots) have one blade-shaped cotyledon, whereas dicotyledons (dicots) possess two round cotyledons. Gymnosperms are more varied. For example, pine seedlings have up to eight cotyledons. The seedlings of some flowering plants have no cotyledons at all. These are said to be acotyledons.

The plumule is the part of a seed embryo that develops into the shoot bearing the first true leaves of a plant. In most seeds, for example the sunflower, the plumule is a small conical structure without any leaf structure. Growth of the plumule does not occur until the cotyledons have grown above ground. This is epigeal germination. However, in seeds such as the broad bean, a leaf structure is visible on the plumule in the seed.
