addition to row spacing and seed depth are of great importance. 
Planters work much the same as seeders except instead of air to transport the seed, it is common to see a vacuum system where seed is taken from a large 
hopper to individual hoppers for each row and forced into the ground in neat rows via a short, powerful blast of pressurized air. 
